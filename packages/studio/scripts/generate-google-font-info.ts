const basicInfos: BasicFontInfo[] = [];

for (const importName of TOP_250_GOOGLE_FONT_IMPORT_NAMES) {
	const info = (await import(
		`@remotion/google-fonts/${importName}`
	)) as GoogleFont;
	const fontInfo = info.getInfo();
	const previewUrl = fontInfo.url.match(/family=(.*):/)?.[1];
	if (!previewUrl) {
		throw new Error(
			`Could not extract Google Fonts CSS family for ${fontInfo.fontFamily}`,
		);
	}

	const weights = Object.keys(fontInfo.fonts.normal ?? {}).sort(
		(a, b) => Number(a) - Number(b),
	);

	basicInfos.push({
		importName: fontInfo.importName,
		fontFamily: fontInfo.fontFamily,
		previewUrl,
		weights: weights.length ? weights : ['400'],
	});
}

const basicContent = [
	`// Auto-generated by packages/studio/scripts/generate-google-font-info.ts`,
	`export const GOOGLE_FONTS_LIST = ${JSON.stringify(basicInfos, null, 2)};`,
].join('\n');

writeFileSync(
	path.join(
		__dirname,
		'..',
		'src',
		'components',
		'Timeline',
		'google-fonts-list.ts',
	),
	basicContent,
);
execSync(
	`bunx oxfmt packages/studio/src/components/Timeline/google-fonts-list.ts --write`,
	{
		stdio: 'inherit',
	},
);
