import {readdir, writeFile} from 'node:fs/promises';
import {dirname, join, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';
import {runCommand} from './command';
import {copyDirectory, readJson, writeJson} from './files';

type PackageJson = {
	name?: string;
	dependencies?: Record<string, string>;
	devDependencies?: Record<string, string>;
	[key: string]: unknown;
};

export const packageRoot = resolve(
	dirname(fileURLToPath(import.meta.url)),
	'..',
);
export const repoRoot = resolve(packageRoot, '..', '..');
export const templateBlank = resolve(packageRoot, '..', 'template-blank');
export const skillsSource = resolve(packageRoot, '..', 'skills', 'skills');

const remotionPackagesInBlankTemplate = [
	'@remotion/cli',
	'@remotion/eslint-config-flat',
	'remotion',
];

let publishedRemotionVersionPromise: Promise<string> | null = null;

const getPublishedRemotionVersion = () => {
	publishedRemotionVersionPromise ??= (async () => {
		const override = process.env.REMOTION_SKILLS_EVALS_REMOTION_VERSION;

		if (override) {
			return override;
		}

		const result = await runCommand({
			command: ['bun', 'pm', 'view', 'remotion', 'version'],
			cwd: repoRoot,
			timeoutMs: 30_000,
		});

		if (result.exitCode !== 0) {
			throw new Error(
				`Could not resolve the latest published Remotion version: ${result.stderr}`,
			);
		}

		return result.stdout.trim();
	})();

	return publishedRemotionVersionPromise;
};

const patchDependencyMap = (
	dependencies: Record<string, string> | undefined,
	version: string,
) => {
	if (!dependencies) {
		return dependencies;
	}

	return Object.fromEntries(
		Object.entries(dependencies).map(([name, dependencyVersion]) => [
			name,
			remotionPackagesInBlankTemplate.includes(name)
				? version
				: dependencyVersion,
		]),
	);
};

export const copyBlankTemplate = async ({
	projectRoot,
	projectName,
}: {
	projectRoot: string;
	projectName: string;
}) => {
	await copyDirectory(templateBlank, projectRoot);

	const packageJsonFile = join(projectRoot, 'package.json');
	const packageJson = await readJson<PackageJson>(packageJsonFile);
	const version = await getPublishedRemotionVersion();

	await writeJson(packageJsonFile, {
		...packageJson,
		name: projectName,
		dependencies: patchDependencyMap(packageJson.dependencies, version),
		devDependencies: patchDependencyMap(packageJson.devDependencies, version),
	});

	await writeFile(
		join(projectRoot, 'README.md'),
		`# ${projectName}\n\nGenerated by @remotion/skills-evals.\n`,
	);
};

export const getSkillNames = async (sourcePath = skillsSource) => {
	const entries = await readdir(sourcePath, {withFileTypes: true});

	return entries
		.filter((entry) => entry.isDirectory())
		.map((entry) => entry.name)
		.sort();
};
