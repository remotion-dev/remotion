import {createHash} from 'node:crypto';
import {
	cp,
	mkdir,
	readFile,
	readdir,
	rm,
	stat,
	writeFile,
} from 'node:fs/promises';
import {dirname, extname, join, relative, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';
import type {CommandResult} from './command';
import {runCommand, type CommandOutput} from './command';
import type {
	LoggedCommand,
	SkillEvalManifest,
	SkillEvalResult,
	SkillSnapshot,
	VisualArtifact,
} from './manifest';
import {runPi} from './pi';
import type {SkillEvalScenario} from './scenarios';

export type SkillEvalOutput = CommandOutput & {
	phase: 'install' | 'pi' | 'pi-export';
};

type SkillEvalScenarioInput = SkillEvalScenario & {
	onOutput?: (output: SkillEvalOutput) => void;
	runRoot?: string;
	runLabel?: 'before' | 'after';
	skillsSourcePath?: string;
	skillSnapshot?: Partial<
		Pick<SkillSnapshot, 'comparisonId' | 'gitRef' | 'isWorkingTree' | 'label'>
	>;
};

type PackageJson = {
	name?: string;
	dependencies?: Record<string, string>;
	devDependencies?: Record<string, string>;
	[key: string]: unknown;
};

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const repoRoot = resolve(packageRoot, '..', '..');
const runsRoot = resolve(packageRoot, '.runs');
const templateBlank = resolve(packageRoot, '..', 'template-blank');
const skillsSource = resolve(packageRoot, '..', 'skills', 'skills');
const remotionPackagesInBlankTemplate = [
	'@remotion/cli',
	'@remotion/eslint-config-flat',
	'remotion',
];
const visualArtifactExtensions = new Set([
	'.gif',
	'.jpeg',
	'.jpg',
	'.mov',
	'.mp4',
	'.png',
	'.webm',
]);
const visualArtifactTypePriority: Record<VisualArtifact['type'], number> = {
	video: 0,
	image: 1,
};

const createTimestamp = () => new Date().toISOString().replace(/[:.]/g, '-');

const sanitizePathPart = (value: string) => {
	return value
		.toLowerCase()
		.replace(/[^a-z0-9._-]+/g, '-')
		.replace(/^-+|-+$/g, '')
		.slice(0, 80);
};

const copyDirectory = async (from: string, to: string) => {
	await rm(to, {force: true, recursive: true});
	await cp(from, to, {
		recursive: true,
		filter: (source) => {
			const pathParts = source.split(/[\\/]/);

			return (
				!pathParts.includes('node_modules') && !pathParts.includes('.DS_Store')
			);
		},
	});
};

const readJson = async <T>(file: string): Promise<T> => {
	return JSON.parse(await readFile(file, 'utf-8')) as T;
};

const writeJson = async (file: string, value: unknown) => {
	await writeFile(file, `${JSON.stringify(value, null, 2)}\n`);
};

const listFilesRecursively = async (dir: string): Promise<string[]> => {
	const entries = await readdir(dir, {withFileTypes: true});
	const files = await Promise.all(
		entries.map((entry) => {
			const absolutePath = join(dir, entry.name);

			if (entry.isDirectory()) {
				if (entry.name === 'node_modules') {
					return [];
				}

				return listFilesRecursively(absolutePath);
			}

			return [absolutePath];
		}),
	);

	return files.flat().sort();
};

const hashDirectory = async (dir: string) => {
	const hash = createHash('sha256');
	const files = await listFilesRecursively(dir);

	for (const file of files) {
		const stats = await stat(file);

		if (!stats.isFile()) {
			continue;
		}

		hash.update(relative(dir, file));
		hash.update('\0');
		hash.update((await readFile(file)).toString('base64'));
		hash.update('\0');
	}

	return hash.digest('hex').slice(0, 12);
};

const getCurrentRemotionVersion = async () => {
	const versionFile = await readFile(
		join(repoRoot, 'packages', 'core', 'src', 'version.ts'),
		'utf-8',
	);
	const match = versionFile.match(/VERSION = '([^']+)'/);

	if (!match) {
		throw new Error('Could not read the current Remotion version.');
	}

	return match[1];
};

const patchDependencyMap = (
	dependencies: Record<string, string> | undefined,
	version: string,
) => {
	if (!dependencies) {
		return dependencies;
	}

	return Object.fromEntries(
		Object.entries(dependencies).map(([name, dependencyVersion]) => [
			name,
			remotionPackagesInBlankTemplate.includes(name)
				? version
				: dependencyVersion,
		]),
	);
};

const copyBlankTemplate = async ({
	projectRoot,
	projectName,
}: {
	projectRoot: string;
	projectName: string;
}) => {
	await copyDirectory(templateBlank, projectRoot);

	const packageJsonFile = join(projectRoot, 'package.json');
	const packageJson = await readJson<PackageJson>(packageJsonFile);
	const version = await getCurrentRemotionVersion();

	await writeJson(packageJsonFile, {
		...packageJson,
		name: projectName,
		dependencies: patchDependencyMap(packageJson.dependencies, version),
		devDependencies: patchDependencyMap(packageJson.devDependencies, version),
	});

	await writeFile(
		join(projectRoot, 'README.md'),
		`# ${projectName}\n\nGenerated by @remotion/skills-evals.\n`,
	);
};

const copySkillsForPiDiscovery = async ({
	projectRoot,
	skillSnapshot,
	skillsSourcePath,
}: {
	projectRoot: string;
	skillSnapshot?: SkillEvalScenarioInput['skillSnapshot'];
	skillsSourcePath?: string;
}): Promise<SkillSnapshot> => {
	const projectSkillsRoot = join(projectRoot, '.pi', 'skills');
	const sandboxPath = join(projectSkillsRoot, 'remotion');
	const sourcePath = skillsSourcePath ?? skillsSource;

	await mkdir(projectSkillsRoot, {recursive: true});
	await copyDirectory(join(sourcePath, 'remotion'), sandboxPath);

	return {
		...skillSnapshot,
		sourcePath,
		sandboxPath,
		hash: await hashDirectory(sandboxPath),
	};
};

const discoverVisualArtifacts = async (
	projectRoot: string,
): Promise<VisualArtifact[]> => {
	const files = await listFilesRecursively(projectRoot);
	const artifacts = await Promise.all(
		files
			.filter((file) =>
				visualArtifactExtensions.has(extname(file).toLowerCase()),
			)
			.map(async (file) => {
				const stats = await stat(file);
				const extension = extname(file).toLowerCase();

				return {
					path: file,
					relativePath: relative(projectRoot, file),
					sizeInBytes: stats.size,
					type:
						extension === '.png' ||
						extension === '.jpg' ||
						extension === '.jpeg'
							? 'image'
							: 'video',
				} satisfies VisualArtifact;
			}),
	);

	return artifacts.sort(
		(a, b) =>
			visualArtifactTypePriority[a.type] - visualArtifactTypePriority[b.type] ||
			a.relativePath.localeCompare(b.relativePath),
	);
};

const logCommand = async ({
	command,
	logDir,
	name,
}: {
	command: CommandResult;
	logDir: string;
	name: string;
}): Promise<LoggedCommand> => {
	const stdoutPath = join(logDir, `${name}.stdout.log`);
	const stderrPath = join(logDir, `${name}.stderr.log`);

	await writeFile(stdoutPath, command.stdout);
	await writeFile(stderrPath, command.stderr);

	return {
		command: command.command,
		cwd: command.cwd,
		durationMs: command.durationMs,
		exitCode: command.exitCode,
		stdoutPath,
		stderrPath,
	};
};

export const runSkillEval = async (
	input: SkillEvalScenarioInput,
): Promise<SkillEvalResult> => {
	const runRoot = input.runRoot ?? runsRoot;
	const runLabel = input.runLabel ? `${input.runLabel}--` : '';
	const runDir = join(
		runRoot,
		sanitizePathPart(input.id),
		`${createTimestamp()}--${runLabel}${sanitizePathPart(input.model)}`,
	);
	const projectRoot = join(runDir, 'project');
	const sessionDir = join(runDir, 'pi-session');
	const logDir = join(runDir, 'logs');
	const createdAt = new Date().toISOString();

	await rm(runDir, {force: true, recursive: true});
	await mkdir(runDir, {recursive: true});
	await mkdir(logDir, {recursive: true});
	await copyBlankTemplate({
		projectName: `skills-eval-${sanitizePathPart(input.id)}`,
		projectRoot,
	});
	const skillSnapshot = await copySkillsForPiDiscovery({
		projectRoot,
		skillSnapshot: input.skillSnapshot,
		skillsSourcePath: input.skillsSourcePath,
	});
	const install = await runCommand({
		command: ['bun', 'install'],
		cwd: projectRoot,
		onOutput: (output) => input.onOutput?.({...output, phase: 'install'}),
	});

	if (install.exitCode !== 0) {
		throw new Error(
			`bun install failed (${install.exitCode}): ${install.stderr}`,
		);
	}

	const pi = await runPi({
		model: input.model,
		onOutput: input.onOutput,
		projectRoot,
		prompt: input.prompt,
		sessionDir,
		timeoutMs: input.timeoutMs,
	});
	const artifacts = await discoverVisualArtifacts(projectRoot);

	if (artifacts.length === 0) {
		throw new Error(
			`No visual artifacts were found in ${relative(repoRoot, projectRoot)}.`,
		);
	}

	const completedAt = new Date().toISOString();
	const manifest: SkillEvalManifest = {
		id: input.id,
		model: input.model,
		prompt: input.prompt,
		createdAt,
		completedAt,
		runDir,
		projectRoot,
		skillSnapshot,
		pi: {
			sessionFile: pi.sessionFile,
			htmlExport: pi.htmlExport,
		},
		artifacts,
		commands: {
			install: await logCommand({command: install, logDir, name: 'install'}),
			pi: await logCommand({command: pi.command, logDir, name: 'pi'}),
			piExport: await logCommand({
				command: pi.exportCommand,
				logDir,
				name: 'pi-export',
			}),
		},
	};
	const manifestPath = join(runDir, 'manifest.json');

	await writeJson(manifestPath, manifest);

	return {
		manifest,
		manifestPath,
		scenario: input,
	};
};
